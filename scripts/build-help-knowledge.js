#!/usr/bin/env node
/**
 * Bundles wiki/*.md into a JS module so the Ask Jenny API route
 * can read help docs at runtime without filesystem access on Netlify.
 *
 * Runs automatically via `prebuild`. Output is gitignored.
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const WIKI_DIR = path.join(ROOT, 'wiki');
const OUT_FILE = path.join(ROOT, 'src', 'lib', 'help-knowledge.generated.js');

function titleFromMarkdown(md, fallback) {
  const m = md.match(/^#\s+(.+)$/m);
  return m ? m[1].trim() : fallback;
}

function articleSlugFromFilename(filename) {
  return filename.replace(/\.md$/, '');
}

function main() {
  if (!fs.existsSync(WIKI_DIR)) {
    console.error(`No wiki/ directory at ${WIKI_DIR} — writing empty bundle.`);
    fs.writeFileSync(OUT_FILE, 'module.exports = { articles: [] };\n');
    return;
  }

  const files = fs.readdirSync(WIKI_DIR)
    .filter((f) => f.endsWith('.md'))
    .sort();

  const articles = files.map((filename) => {
    const content = fs.readFileSync(path.join(WIKI_DIR, filename), 'utf8');
    return {
      slug: articleSlugFromFilename(filename),
      title: titleFromMarkdown(content, articleSlugFromFilename(filename).replace(/-/g, ' ')),
      content: content.trim(),
    };
  });

  const banner = '// AUTO-GENERATED by scripts/build-help-knowledge.js — do not edit.\n';
  const body = `module.exports = ${JSON.stringify({ articles }, null, 2)};\n`;
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, banner + body);
  console.log(`Wrote ${articles.length} articles to ${path.relative(ROOT, OUT_FILE)}`);
}

main();
